const all = [...tvShows, ...movies];

const existing = db.prepare('SELECT title FROM movies').all().map(m => m.title.toLowerCase());

const missing = [];
const found = [];

for (const title of all) {
    const lower = title.toLowerCase();
    const match = existing.some(e => e.includes(lower) || lower.includes(e) || e === lower);
    if (match) {
        found.push(title);
    } else {
        missing.push(title);
    }
}

console.log(`=== FOUND (${found.length}) ===`);
