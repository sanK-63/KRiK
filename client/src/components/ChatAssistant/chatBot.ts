const HISTORY_KEY = "knight-chat-history";
const GREETED_KEY = "knight-chat-greeted";

export function loadHistory(): ChatMessage[] {
    try {
        const raw = localStorage.getItem(HISTORY_KEY);
        if (!raw) return [];
        return JSON.parse(raw);
    } catch {
        return [];
    }
}

export function saveHistory(messages: ChatMessage[]) {
    const trimmed = messages.slice(-50);
    localStorage.setItem(HISTORY_KEY, JSON.stringify(trimmed));
}

export function hasGreeted(): boolean {
    return localStorage.getItem(GREETED_KEY) === "true";
}

export function markGreeted() {
    localStorage.setItem(GREETED_KEY, "true");
}
