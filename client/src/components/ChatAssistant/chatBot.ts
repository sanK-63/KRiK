function normalize(text: string): string {
    return text.toLowerCase().replace(/[ё]/g, "е").replace(/[^а-яa-z0-9\s]/g, "").trim();
}

function matchRule(input: string, rule: Rule): boolean {
    const norm = normalize(input);
    return rule.keywords.some((kw) => norm.includes(normalize(kw)));
}

export function getBotReply(
    userText: string,
    userName: string
): { text: string; buttons?: QuickButton[] } {
    for (const rule of rules) {
        if (matchRule(userText, rule)) {
            const text = typeof rule.reply === "function" ? rule.reply(userName) : rule.reply;
            return { text, buttons: rule.buttons };
        }
    }

    return {
        text: `Я пока не знаю ответа на этот вопрос. Вот что я умею:\n\n• Подсказать по разделам сайта\n• Объяснить как пользоваться функциями\n• Направить к нужной странице\n\nСпроси, например: «Как создать турнир?»`,
    };
}

