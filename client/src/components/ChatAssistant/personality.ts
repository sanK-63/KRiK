const LAST_CATEGORY_KEY = "knight-last-category";

function setLastCategory(category: Category) {
    try {
        localStorage.setItem(LAST_CATEGORY_KEY, category);
    } catch {
        // ignore
    }
}

function pickRandom<T>(arr: T[], exclude?: T | null): T {
    if (arr.length === 1) return arr[0];
    const filtered = exclude != null ? arr.filter((item) => item !== exclude) : arr;
    if (filtered.length === 0) return arr[Math.floor(Math.random() * arr.length)];
    return filtered[Math.floor(Math.random() * filtered.length)];
}

export function getTimeOfDay(): "morning" | "afternoon" | "evening" | "night" {
    const hour = new Date().getHours();
    if (hour >= 6 && hour < 12) return "morning";
    if (hour >= 12 && hour < 18) return "afternoon";
    if (hour >= 18 && hour < 24) return "evening";
    return "night";
}

export function getGreetingText(userName: string): string {
    const time = getTimeOfDay();
    const greetings = personalityData.greeting;

    let timePrefix = "";
    switch (time) {
        case "morning":
            timePrefix = "Доброе утро! ";
            break;
        case "afternoon":
            timePrefix = "Добрый день! ";
            break;
        case "evening":
            timePrefix = "Добрый вечер! ";
            break;
        case "night":
            timePrefix = "Ты ещё не спишь? Ладно, я тоже не сплю. Но у меня хотя бы оправдание — я бот. ";
            break;
    }

    const entry = pickRandom(greetings, null);
    setLastCategory("greeting");

    const text = typeof entry.text === "function" ? entry.text(userName) : entry.text;
    return `${timePrefix}${text}`;
}

export function getPersonalityReply(
    category: Category,
    userName?: string
): string {
    const entries = personalityData[category];
    if (!entries || entries.length === 0) return "";

    const entry = pickRandom(entries, null);
    setLastCategory(category);

    const text = typeof entry.text === "function" ? entry.text(userName || "Рыцарь") : entry.text;
    return text;
}

export function getSobererLevel(): number {
    try {
        const raw = localStorage.getItem("knight-error-count");
        const count = raw ? parseInt(raw, 10) : 0;
        return count;
    } catch {
        return 0;
    }
}

export function incrementErrorCount() {
    try {
        const raw = localStorage.getItem("knight-error-count");
        const count = raw ? parseInt(raw, 10) + 1 : 1;
        localStorage.setItem("knight-error-count", count.toString());
    } catch {
        // ignore
    }
}

export function resetErrorCount() {
    try {
        localStorage.setItem("knight-error-count", "0");
    } catch {
        // ignore
    }
}

export function getSmartReply(userText: string, userName: string): { text: string; category: Category } {
    const lower = userText.toLowerCase();

    if (/лень|не хочу|не буду|устал|надоело|нет настроения|неинтересно/.test(lower)) {
        return { text: getPersonalityReply("procrastination", userName), category: "procrastination" };
    }
    if (/спасибо|благодарю|спс|thanks|респект|премного/.test(lower)) {
        return { text: getPersonalityReply("special", userName), category: "special" };
    }
    if (/конституция|правила|регламент|устав|закон/.test(lower)) {
        return { text: getPersonalityReply("discipline", userName), category: "discipline" };
    }
    if (/я лучший|я гений|мой код идеален|я всё могу|я король/.test(lower)) {
        return { text: getPersonalityReply("soberer", userName), category: "soberer" };
    }
    if (/почему|зачем|как так|объясни|не понимаю/.test(lower)) {
        return { text: getPersonalityReply("advice", userName), category: "advice" };
    }
    if (/помоги|совет|что делать|как быть/.test(lower)) {
        return { text: getPersonalityReply("advice", userName), category: "advice" };
    }

    return { text: getPersonalityReply("quip", userName), category: "quip" };
}
